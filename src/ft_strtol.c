#include <libft.h>

long	ft_strtol(char *str)
{
	long	res;

	res = 0;
	str += 2;
	while (*str)
	{
		res *= 16;
		if (*str >= 'A' && *str <= 'F')
			res += *str - 'A' + 10;
		else
			res += *str - '0';
		str++;
	}
	return (res);
}
